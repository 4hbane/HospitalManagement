package com.ENSAM.CRMHopital.Medicaments;

public enum MedicamentType {
    GENERIQUE,             // Est un médicament identique ou équivalent à celui d'une marque
    BIOSIMILAIRE,         // Sont des médicaments produits par biotechnologie dont le brevet a expiré
    ORPHELIN,            //  Tout médicament développé pour le traitement de "maladies orphelines"
    BIOLOGIQUE,         //  Biologique ou biomédicament est un produit biotechnologique, pharmaceuticalement actif et synthétisé par une source biologique ou extraite d'elle, et non obtenue par la chimie de synthèse
    A_BASE_DE_PLANTES, // Désigne le traitement thérapeutique fondé sur les extraits de plantes et les principes actifs naturels, dans le but de guérir, soulager ou prévenir une maladie.
    ESSENTIEL         // Définis par l'Organisation mondiale de la santé (OMS) comme ceux qui satisfont aux besoins de santé de la majorité de la population.
    }
